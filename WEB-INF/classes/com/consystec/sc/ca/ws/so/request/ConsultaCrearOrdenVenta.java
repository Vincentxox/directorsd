package com.consystec.sc.ca.ws.so.request;

public class ConsultaCrearOrdenVenta {
	
	private String idCliente;
	private String idCanalDistribucion;
	private String idUbicacion;
	private String idOffering;
	private String usuario;
	private String pais;
	
	public String getIdCliente() {
		return idCliente;
	}
	public void setIdCliente(String idCliente) {
		this.idCliente = idCliente;
	}
	public String getIdCanalDistribucion() {
		return idCanalDistribucion;
	}
	public void setIdCanalDistribucion(String idCanalDistribucion) {
		this.idCanalDistribucion = idCanalDistribucion;
	}
	public String getIdUbicacion() {
		return idUbicacion;
	}
	public void setIdUbicacion(String idUbicacion) {
		this.idUbicacion = idUbicacion;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getPais() {
		return pais;
	}
	public void setPais(String pais) {
		this.pais = pais;
	}
	public String getIdOffering() {
		return idOffering;
	}
	public void setIdOffering(String idOffering) {
		this.idOffering = idOffering;
	}
	
	
}